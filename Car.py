import turtle
wn=turtle.Screen()
t=turtle.Turtle()
t.width(6)
wn.bgcolor('lightgreen')
t.penup()
t.setx(-200)
t.sety(0)
t.pendown()
t.forward(50)
t.penup()
t.goto(-100,-50)
def circle():
    t.pendown()
    t.color('black', 'orange')
    t.begin_fill()
    t.circle(50)
    t.end_fill()
    t.penup()
circle()
for i in range(10):
    t.penup
    t.goto(-100,0)
    t.pendown()
    t.left(36)
    t.forward(50)
    t.backward(50)
t.goto(-50,0)
t.pendown()
t.forward(200)
t.penup()
t.goto(200,-50)
circle()
for i in range(10):
    t.penup()
    t.goto(200,0)
    t.pendown()
    t.left(36)
    t.forward(50)
    t.backward(50)
t.goto(250,0)
t.pendown()
t.forward(50)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.right(60)
t.forward(100)
t.left(60)
t.forward(200)
t.left(60)
t.forward(100)
t.left(120)
t.fillcolor('orange')
t.forward(150)
t.left(90)
t.forward(88)
t.backward(88)
t.right(90)
t.forward(150)
t.backward(400)
t.right(90)
t.forward(100)
t.penup()
t.goto(-300,-50)
t.pendown()
t.left(90)
t.forward(700)
t.pendown()
turtle.done()